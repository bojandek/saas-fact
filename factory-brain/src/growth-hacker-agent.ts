// @ts-nocheck
import { getLLMClient, CLAUDE_MODELS } from './llm/client'
import { RAGSystem } from './rag';
import { WarRoomOrchestrator, AgentMessage, AgentContext } from './war-room-orchestrator';
import { logger } from './utils/logger'
import { GROWTH_HACKER_PROMPT } from './prompts/agent-prompts'

export interface MarketResearch {
  trendingTopics: string[];
  competitorMoves: string[];
  customerPainPoints: string[];
  platformInsights: {
    reddit: string[];
    twitter: string[];
    hackerNews: string[];
  };
}

export interface GrowthPlan {
  seo: {
    metaTitle: string;
    metaDescription: string;
    keywords: string[];
  };
  socialMediaPosts: Array<{
    platform: string;
    content: string;
    hashtags: string[];
  }>;
  emailCampaign: Array<{
    subject: string;
    body: string;
  }>;
}

export class GrowthHackerAgent {
  private llm = getLLMClient();
  private ragSystem: RAGSystem;
  private orchestrator?: WarRoomOrchestrator;

  constructor(orchestrator?: WarRoomOrchestrator) {
    this.llm = getLLMClient();
    this.ragSystem = new RAGSystem();
    this.orchestrator = orchestrator;
  }

  async researchLast30Days(niche: string): Promise<MarketResearch> {
    if (this.orchestrator) {
      await this.orchestrator.sendMessage({
        sender: 'AI Growth Hacker Agent',
        recipient: 'Orchestrator',
        type: 'info',
        content: `Conducting /last30days market research for niche: ${niche} across Reddit, X, HackerNews...`,
      });
    }

    const prompt = `Perform a simulated /last30days market intelligence research for the SaaS niche: "${niche}".
Analyze recent trends, discussions, and complaints on Reddit, X (Twitter), HackerNews, and YouTube.
Provide the output as a JSON object with the following structure:
{
  "trendingTopics": ["string"],
  "competitorMoves": ["string"],
  "customerPainPoints": ["string"],
  "platformInsights": {
    "reddit": ["string"],
    "twitter": ["string"],
    "hackerNews": ["string"]
  }
}`;

    const response = await this.llm.chat({
      model: CLAUDE_MODELS.SONNET, // Use a more capable model for research
      messages: [
        { role: "system", content: "You are a real-time market intelligence agent. Synthesize realistic recent trends for the given niche." },
        { role: "user", content: prompt },
      ],
      temperature: 0.7,
      max_tokens: 2000,
      response_format: { type: "json_object" },
    });

    const researchJson = response.content;
    if (!researchJson) {
      throw new Error("Failed to generate market research.");
    }

    try {
      const research: MarketResearch = JSON.parse(researchJson);
      if (this.orchestrator) {
        await this.orchestrator.sendMessage({
          sender: 'AI Growth Hacker Agent',
          recipient: 'Orchestrator',
          type: 'response',
          content: 'Market research completed successfully.',
          payload: research,
        });
        this.orchestrator.updateContext({ marketResearch: research });
      }
      return research;
    } catch (e) {
      logger.error("Failed to parse market research JSON:", researchJson, e);
      throw new Error("Invalid market research format generated by AI.");
    }
  }

  async generateGrowthPlan(saasDescription: string, currentContext?: AgentContext & { marketResearch?: MarketResearch }): Promise<GrowthPlan> {
    if (this.orchestrator) {
      await this.orchestrator.sendMessage({
        sender: 'AI Growth Hacker Agent',
        recipient: 'Orchestrator',
        type: 'info',
        content: `Starting growth plan generation for: ${saasDescription}`,
      });
    }

    let refinedDescription = saasDescription;
    if (currentContext?.saasDescription) {
      refinedDescription = currentContext.saasDescription;
    }

    // Retrieve relevant growth and retention principles from the knowledge base
    const growthPrinciples = await this.ragSystem.search(
      "SaaS Growth & Retention Best Practices",
      2
    );
    const growthContext = growthPrinciples
      .map((doc) => doc.content)
      .join("\n\n");

    let researchContext = "";
    if (currentContext?.marketResearch) {
      researchContext = `\nRecent Market Intelligence (/last30days):\n${JSON.stringify(currentContext.marketResearch, null, 2)}\nUse these recent trends and pain points to make the growth plan highly relevant and timely.\n`;
    }

    const prompt = `Generate a comprehensive growth plan for a SaaS application based on the following description. The plan should include SEO strategy, social media posts, and an email campaign. Adhere to the provided growth and retention best practices to ensure sustainable success.

Description: ${refinedDescription}
${researchContext}
Growth & Retention Context:\n${growthContext}\n
Provide the output as a JSON object with the following structure:
{
  "seo": {
    "metaTitle": "string",
    "metaDescription": "string",
    "keywords": ["string"]
  },
  "socialMediaPosts": [
    {
      "platform": "string" (e.g., "LinkedIn", "Twitter"),
      "content": "string",
      "hashtags": ["string"]
    }
  ],
  "emailCampaign": [
    {
      "subject": "string",
      "body": "string"
    }
  ]
}

Ensure the content is engaging, relevant, and optimized for growth.`;

    const response = await this.llm.chat({
      model: CLAUDE_MODELS.HAIKU, // Use a suitable model
      messages: [
        { role: "system", content: GROWTH_HACKER_PROMPT },
        { role: "user", content: prompt },
      ],
      temperature: 0.7,
      max_tokens: 1500,
      response_format: { type: "json_object" },
    });

    const planJson = response.content;
    if (!planJson) {
      if (this.orchestrator) {
        await this.orchestrator.sendMessage({
          sender: 'AI Growth Hacker Agent',
          recipient: 'Orchestrator',
          type: 'critical',
          content: 'Failed to generate growth plan content.',
        });
      }
      throw new Error("Failed to generate growth plan.");
    }

    try {
      const plan: GrowthPlan = JSON.parse(planJson);
      if (this.orchestrator) {
        await this.orchestrator.sendMessage({
          sender: 'AI Growth Hacker Agent',
          recipient: 'Orchestrator',
          type: 'response',
          content: 'Growth plan generated successfully.',
          payload: plan,
        });
        this.orchestrator.updateContext({ growthPlan: plan });
      }
      return plan;
    } catch (e) {
      logger.error("Failed to parse generated growth plan JSON:", planJson, e);
      if (this.orchestrator) {
        await this.orchestrator.sendMessage({
          sender: 'AI Growth Hacker Agent',
          recipient: 'Orchestrator',
          type: 'critical',
          content: `Invalid growth plan format generated by AI: ${planJson}`,
        });
      }
      throw new Error("Invalid growth plan format generated by AI.");
    }
  }
}
